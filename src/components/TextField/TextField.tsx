import classNames from 'classnames';
import React, { useState } from 'react';

type Props = {
  name: string;
  value: string;
  label?: string;
  placeholder?: string;
  required?: boolean;
  onChange?: (newValue: React.ChangeEvent<HTMLInputElement>) => void;
};

function getRandomDigits() {
  return Math.random().toFixed(16).slice(2);
}

export const TextField: React.FC<Props> = ({
  name,
  value,
  label = name,
  placeholder = `Enter ${label}`,
  required = false,
  onChange = () => {},
}) => {
  // generate a unique id once on component load
  const [id] = useState(() => `${name}-${getRandomDigits()}`);

  // To show errors only if the field was touched (onBlur)
  const [touched, setTouched] = useState(false);
  const handleBlur = () => {
    setTouched(true);
  };

  const hasError = touched && required && !value;
  // eslint-disable-next-line max-len, prettier/prettier
  const pattern = /^((([A-Za-z]{3,9}:(?:\/\/)?)(?:[-;:&=+$,\w]+@)?[A-Za-z0-9.-]+|(?:www\.|[-;:&=+$,\w]+@)[A-Za-z0-9.-]+)((?:\/[+~%/.\w-_]*)?\??(?:[-+=&;%@,.\w_]*)#?(?:[,.!/\\\w]*))?)$/;
  const errorSpace = touched && !value.trim() && !hasError && required;
  const errorLink =
    touched &&
    (name === 'imgUrl' || name === 'imdbUrl') &&
    !pattern.test(value) &&
    !hasError &&
    !errorSpace;

  return (
    <div className="field">
      <label className="label" htmlFor={id}>
        {label}
      </label>

      <div className="control">
        <input
          name={name}
          type="text"
          id={id}
          data-cy={`movie-${name}`}
          className={classNames('input', {
            'is-danger': hasError || errorLink || errorSpace,
          })}
          placeholder={placeholder}
          value={value}
          onChange={onChange}
          onBlur={handleBlur}
        />
      </div>

      {hasError && <p className="help is-danger">{`${label} is required`}</p>}
      {errorLink && <p className="help is-danger">{`${label} must be url`}</p>}
      {errorSpace && (
        <p className="help is-danger">{`Space is not ${label}`}</p>
      )}
    </div>
  );
};
